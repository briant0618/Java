public class P53 {

	public static void main(String[] args) {
		int num = 10;
		System.out.println("num = " + num);
		num += 10;
		System.out.println("num = " + num);
		num -= 10;
		System.out.println("num = " + num);
		num *= 2;
		System.out.println("num = " + num);
		num /= 3;
		System.out.println("num = " + num);
		num %= 3;
		System.out.println("num = " + num);

	}

}
